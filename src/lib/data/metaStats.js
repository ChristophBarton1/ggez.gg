// Helper to get stats safely
export function getChampionStats(name) {
    // Normalize name (remove spaces, apostrophes)
    const key = name.replace(/[^a-zA-Z]/g, '');
    
    // Find exact match or case insensitive match in our snapshot
    const exactMatch = metaStats[key] || metaStats[name];
    if (exactMatch) return exactMatch;
    
    // Fallback for missing champs (new ones or edge cases)
    // Default to "Balanced" stats
    return { 
        winRate: 50.0, 
        pickRate: 5.0, 
        banRate: 2.0, 
        tier: "B" 
    };
}

// Get all champion stats with championId included
export function getAllChampionStats() {
    return Object.entries(metaStats).map(([name, stats]) => ({
        name,
        championId: championNameToId[name] || 0,
        ...stats
    }));
}
